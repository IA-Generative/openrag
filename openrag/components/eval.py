"""RAG evaluation engine.

Runs Q&A entries through the RAG pipeline and scores responses
using semantic similarity and LLM-as-judge.
"""

import asyncio
from datetime import datetime

from components.indexer.vectordb.utils import QAEntry, QAEvalRun
from config import load_config
from openai import AsyncOpenAI
from utils.logger import get_logger

logger = get_logger()
config = load_config()

EVAL_JUDGE_PROMPT = """You are an evaluation judge. Compare the expected answer with the actual answer generated by a RAG system.

Score the actual answer from 1 to 5:
- 5: Perfectly matches the expected answer in meaning and completeness
- 4: Mostly correct with minor differences
- 3: Partially correct, captures the main idea but misses details
- 2: Somewhat related but significantly different
- 1: Completely wrong or irrelevant

Respond with ONLY a JSON object:
{"score": <1-5>, "reason": "<brief explanation>"}
"""


async def run_evaluation(eval_run_id: int, session_factory):
    """Execute an evaluation run asynchronously.

    Args:
        eval_run_id: ID of the QAEvalRun record
        session_factory: SQLAlchemy Session factory
    """
    from components.pipeline import RagPipeline

    pipeline = RagPipeline()
    llm_client = AsyncOpenAI(base_url=config.llm["base_url"], api_key=config.llm["api_key"])

    with session_factory() as s:
        run = s.query(QAEvalRun).filter_by(id=eval_run_id).first()
        if not run:
            logger.error("Eval run not found", eval_run_id=eval_run_id)
            return

        run.status = "running"
        s.commit()

        # Load matching Q&A entries
        query = s.query(QAEntry).filter(QAEntry.partition_name == run.partition_name)
        run_config = run.config_json or {}
        tags = run_config.get("tags", ["eval"])
        for tag in tags:
            query = query.filter(QAEntry.tags.contains([tag]))
        entries = query.all()

        # Detach entries to avoid session issues
        qa_data = [(e.id, e.question, e.expected_answer, e.partition_name) for e in entries]

    results = {"summary": {}, "details": []}
    scores = []

    for qa_id, question, expected_answer, partition_name in qa_data:
        try:
            # Run through RAG pipeline
            payload = {
                "messages": [{"role": "user", "content": question}],
                "model": config.llm["model"],
            }
            prepared_payload, docs = await pipeline._prepare_for_chat_completion(
                partition=[partition_name], payload=payload
            )
            response = await pipeline.llm_client.chat_completion(prepared_payload)
            actual_answer = response.choices[0].message.content if response.choices else ""

            # LLM-as-judge scoring
            judge_score = 0
            judge_reason = ""
            if expected_answer:
                try:
                    judge_response = await llm_client.chat.completions.create(
                        model=config.llm["model"],
                        messages=[
                            {"role": "system", "content": EVAL_JUDGE_PROMPT},
                            {
                                "role": "user",
                                "content": f"Expected answer:\n{expected_answer}\n\nActual answer:\n{actual_answer}",
                            },
                        ],
                        max_tokens=200,
                    )
                    import json

                    judge_text = judge_response.choices[0].message.content
                    judge_data = json.loads(judge_text)
                    judge_score = judge_data.get("score", 0)
                    judge_reason = judge_data.get("reason", "")
                except Exception as e:
                    logger.warning("LLM judge failed", error=str(e))
                    judge_score = 0
                    judge_reason = f"Judge error: {e}"

            scores.append(judge_score)
            results["details"].append({
                "qa_id": qa_id,
                "question": question,
                "expected": expected_answer,
                "actual": actual_answer,
                "judge_score": judge_score,
                "judge_reason": judge_reason,
            })

        except Exception as e:
            logger.warning("Eval failed for question", qa_id=qa_id, error=str(e))
            results["details"].append({
                "qa_id": qa_id,
                "question": question,
                "expected": expected_answer,
                "actual": "",
                "judge_score": 0,
                "judge_reason": f"Error: {e}",
            })
            scores.append(0)

        # Update progress
        with session_factory() as s:
            run = s.query(QAEvalRun).filter_by(id=eval_run_id).first()
            if run:
                run.completed_questions = len(results["details"])
                s.commit()

    # Finalize
    valid_scores = [sc for sc in scores if sc > 0]
    results["summary"] = {
        "avg_score": round(sum(valid_scores) / len(valid_scores), 2) if valid_scores else 0,
        "pass_rate": round(sum(1 for sc in valid_scores if sc >= 3) / len(valid_scores), 2) if valid_scores else 0,
        "total": len(scores),
        "evaluated": len(valid_scores),
    }

    with session_factory() as s:
        run = s.query(QAEvalRun).filter_by(id=eval_run_id).first()
        if run:
            run.status = "completed"
            run.completed_at = datetime.now()
            run.results = results
            s.commit()

    logger.info("Eval run completed", eval_run_id=eval_run_id, avg_score=results["summary"]["avg_score"])
